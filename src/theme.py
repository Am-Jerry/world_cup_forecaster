def render_bottom_nav(active: str):
    """
    Render the floating pill navigation bar.

    Why this doesn't use st.markdown(): Streamlit wraps every st.markdown()
    block in an internal container that has a CSS `transform` applied to
    it somewhere up the ancestor chain. Per the CSS spec, any ancestor
    with a transform becomes the new "containing block" for descendant
    position:fixed elements — so a fixed-position nav rendered via
    st.markdown() never actually reaches the real page viewport; it gets
    silently boxed into that transformed container instead, with no error.

    The fix is to skip st.markdown() for this element entirely and inject
    it straight into the real document body with JavaScript, run via a
    zero-height components.html() iframe purely as a vehicle to execute
    that script. window.parent.document is the actual top-level page
    (same-origin, since it's served by the same Streamlit process), so
    position:fixed there behaves normally.

    Call this once near the top of every page (after inject_css()),
    passing which item should be highlighted as active — one of:
    "home", "teams", "groups", "final", "about".
    """
    items_html = ""
    for key, href, label, svg in NAV_ITEMS:
        is_active = key == active
        color = COLORS["accent"] if is_active else "#FFFFFF"
        bg = "#2C2C2E" if is_active else "transparent"
        items_html += (
            f'<a href="{href}" target="_self" style="text-decoration:none;">'
            f'<div style="display:flex;flex-direction:column;align-items:center;'
            f'justify-content:center;width:72px;height:64px;border-radius:28px;'
            f'background:{bg};color:{color};">'
            f'<div style="margin-bottom:3px;">{svg}</div>'
            f'<span style="font-size:10px;font-weight:600;color:{color};">{label}</span>'
            f'</div></a>'
        )

    nav_html = (
        f'<div id="wc-bottom-nav" style="position:fixed;bottom:20px;left:50%;'
        f'transform:translateX(-50%);z-index:999999;width:95%;max-width:460px;">'
        f'<nav style="display:flex;justify-content:space-between;align-items:center;'
        f'padding:8px;background:{COLORS["nav_bg"]};border-radius:40px;'
        f'border:1px solid rgba(255,255,255,0.06);'
        f'box-shadow:0 20px 50px rgba(0,0,0,0.5);">'
        f'{items_html}</nav></div>'
    )

    # Escape backticks/${{ so the HTML is safe to embed in a JS template
    # literal, then inject it into the PARENT document's <body> — not the
    # sandboxed iframe components.html normally renders into — so
    # position:fixed is relative to the real viewport.
    safe_html = nav_html.replace("\\", "\\\\").replace("`", "\\`").replace("${", "\\${")

    injector = (
        "<script>"
        "(function() {"
        "  const doc = window.parent.document;"
        "  const old = doc.getElementById('wc-bottom-nav');"
        "  if (old) old.remove();"
        f"  const wrapper = doc.createElement('div');"
        f"  wrapper.innerHTML = `{safe_html}`;"
        "  doc.body.appendChild(wrapper.firstChild);"
        "})();"
        "</script>"
    )
    components.html(injector, height=0, width=0)



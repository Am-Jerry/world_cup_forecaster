# ---------------------------------------------------------------------------
# Data loading — cached so every page load doesn't re-read the CSV
# ---------------------------------------------------------------------------
RESULTS_PATH = Path("outputs/simulation_results.csv")


@st.cache_data
def load_results() -> pd.DataFrame:
    """
    Load the pre-generated Monte Carlo forecast. Cached for the lifetime
    of the app process — this file never changes without a fresh run of
    main.py, so there's no need to re-read it on every interaction.
    """
    if not RESULTS_PATH.exists():
        st.error(
            "outputs/simulation_results.csv not found. Run main.py locally "
            "and commit that file to the repo before deploying."
        )
        st.stop()
    return pd.read_csv(RESULTS_PATH)


